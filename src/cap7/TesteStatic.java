public class TesteStatic {
	
	static void mostrar(int x ){
		System.out.println("O número é: " +  x);
	}
	static void mostrar(double x ){
		System.out.println("O número double é: " +  x);
	}
	static void mostrar(String nome ){
		System.out.println("O nome é: " +  nome);
	}
	
}
